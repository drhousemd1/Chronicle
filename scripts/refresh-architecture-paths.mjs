const metricsEntries = [...sourceAbsPaths, ...extraAbsPaths]
  .map((absPath) => ({
    webPath: toWebPath(absPath),
    lineCount: countLines(absPath),
  }))
  .sort((a, b) => a.webPath.localeCompare(b.webPath));
const metricsOutput = toMetricsExport(
  "architectureFileMetrics",
  metricsEntries,
  "src/docs/scripts/e2e/supabase/public",
);

const analysisByPath = new Map();

[...sourceAbsPaths, ...extraAbsPaths].forEach((absPath) => {
  const text = readTextIfPossible(absPath);
  const {
    imports,
    tables,
    tableReads,
    tableWrites,
    rpcs,
    edgeFunctions,
    storageBuckets,
    storageReads,
    storageWrites,
    localStorageReads,
    localStorageWrites,
    sessionStorageReads,
    sessionStorageWrites,
  } = extractAstRelationships(absPath, text);

  analysisByPath.set(toWebPath(absPath), {
    imports,
    importedBy: [],
    tables,
    tableReads,
    tableWrites,
    rpcs,
    edgeFunctions,
    storageBuckets,
    storageReads,
    storageWrites,
    localStorageReads,
    localStorageWrites,
    sessionStorageReads,
    sessionStorageWrites,
  });
});

for (const [webPath, analysis] of analysisByPath.entries()) {
  analysis.imports.forEach((importedPath) => {
    const target = analysisByPath.get(importedPath);
    if (!target) return;
    target.importedBy.push(webPath);
  });
}

const analysisEntries = Array.from(analysisByPath.entries())
  .map(([webPath, analysis]) => ({
    webPath,
    imports: uniqueSorted(analysis.imports),
    importedBy: uniqueSorted(analysis.importedBy),
    tables: analysis.tables,
    tableReads: analysis.tableReads,
    tableWrites: analysis.tableWrites,
    rpcs: analysis.rpcs,
    edgeFunctions: analysis.edgeFunctions,
    storageBuckets: analysis.storageBuckets,
    storageReads: analysis.storageReads,
    storageWrites: analysis.storageWrites,
    localStorageReads: analysis.localStorageReads,
    localStorageWrites: analysis.localStorageWrites,
    sessionStorageReads: analysis.sessionStorageReads,
    sessionStorageWrites: analysis.sessionStorageWrites,
  }))
  .sort((a, b) => a.webPath.localeCompare(b.webPath));

const analysisOutput = toAnalysisExport(
  "architectureFileAnalysis",
  analysisEntries,
  "src/docs/scripts/e2e/supabase/public",
);

fs.writeFileSync(path.join(srcRoot, "data", "architecture-source-paths.ts"), sourceOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-extra-paths.ts"), extraOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-file-metrics.ts"), metricsOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-file-analysis.ts"), analysisOutput, "utf8");

console.log(
  `Generated ${sourcePaths.length} source paths, ${extraPaths.length} extra paths, ${metricsEntries.length} line-count metrics, and ${analysisEntries.length} relationship analyses.`,
);
