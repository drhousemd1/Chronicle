function extractAstRelationships(absPath, text) {
  if (!isAstCodeFile(absPath) || !text.trim()) {
    return {
      imports: [],
      tables: [],
      tableReads: [],
      tableWrites: [],
      rpcs: [],
      edgeFunctions: [],
      storageBuckets: [],
      storageReads: [],
      storageWrites: [],
      localStorageReads: [],
      localStorageWrites: [],
      sessionStorageReads: [],
      sessionStorageWrites: [],
    };
  }

  const sourceFile = ts.createSourceFile(
    absPath,
    text,
    ts.ScriptTarget.Latest,
    true,
    getScriptKind(absPath),
  );

  const imports = [];
  const tables = [];
  const tableReads = [];
  const tableWrites = [];
  const rpcs = [];
  const edgeFunctions = [];
  const storageBuckets = [];
  const storageReads = [];
  const storageWrites = [];
  const localStorageReads = [];
  const localStorageWrites = [];
  const sessionStorageReads = [];
  const sessionStorageWrites = [];

  const stringConstants = new Map();
  const stringArrayConstants = new Map();
  const stringReturnFunctions = new Map();
  const browserStorageProxyFunctions = new Map();

  function collectStringConstants(node) {
    if (
      ts.isVariableDeclaration(node) &&
      ts.isIdentifier(node.name) &&
      node.initializer &&
      ts.isStringLiteralLike(node.initializer)
    ) {
      stringConstants.set(node.name.text, node.initializer.text.trim());
    }

    if (
      ts.isVariableDeclaration(node) &&
      ts.isIdentifier(node.name) &&
      node.initializer &&
      ts.isArrayLiteralExpression(node.initializer)
    ) {
      const values = node.initializer.elements
        .map((element) => ts.isStringLiteralLike(element) ? element.text.trim() : null)
        .filter(Boolean);
      if (values.length === node.initializer.elements.length && values.length > 0) {
        stringArrayConstants.set(node.name.text, values);
      }
    }

    ts.forEachChild(node, collectStringConstants);
  }

  function unwrapExpression(expr) {
    let current = expr;
    while (
      ts.isParenthesizedExpression(current) ||
      ts.isAsExpression(current) ||
      ts.isTypeAssertionExpression(current) ||
      ts.isNonNullExpression(current)
    ) {
      current = current.expression;
    }
    return current;
  }

  function compactExpressionText(expr) {
    return expr.getText(sourceFile).replace(/\s+/g, " ").trim();
  }

  function getStringExpressionLabel(expr, options = {}) {
    const {
      allowDynamic = false,
      allowBareIdentifierDynamic = false,
      paramValues = new Map(),
      depth = 0,
    } = options;
    if (depth > 8) return null;

    const current = unwrapExpression(expr);

    if (ts.isStringLiteralLike(current) || ts.isNoSubstitutionTemplateLiteral(current)) {
      return current.text.trim();
    }

    if (ts.isIdentifier(current)) {
      if (paramValues.has(current.text)) return paramValues.get(current.text);
      if (stringConstants.has(current.text)) return stringConstants.get(current.text);
      return allowDynamic && allowBareIdentifierDynamic ? `{${current.text}}` : null;
    }

    if (ts.isTemplateExpression(current)) {
      let label = current.head.text;
      for (const span of current.templateSpans) {
        const spanLabel = getStringExpressionLabel(span.expression, {
          allowDynamic,
          allowBareIdentifierDynamic: true,
          paramValues,
          depth: depth + 1,
        });
        if (!spanLabel) return null;
        label += spanLabel + span.literal.text;
      }
      return label.trim();
    }

    if (
      ts.isBinaryExpression(current) &&
      current.operatorToken.kind === ts.SyntaxKind.PlusToken
    ) {
      const left = getStringExpressionLabel(current.left, {
        allowDynamic,
        allowBareIdentifierDynamic: true,
        paramValues,
        depth: depth + 1,
      });
      const right = getStringExpressionLabel(current.right, {
        allowDynamic,
        allowBareIdentifierDynamic: true,
        paramValues,
        depth: depth + 1,
      });
      if (!left || !right) return null;
      return `${left}${right}`.trim();
    }

    if (ts.isCallExpression(current) && ts.isIdentifier(current.expression)) {
      const builder = stringReturnFunctions.get(current.expression.text);
      if (builder) {
        const nextParamValues = new Map(paramValues);
        builder.params.forEach((paramName, index) => {
          const arg = current.arguments[index];
          const argLabel = arg
            ? getStringExpressionLabel(arg, {
                allowDynamic: true,
                allowBareIdentifierDynamic: true,
                paramValues,
                depth: depth + 1,
              })
            : null;
          nextParamValues.set(paramName, argLabel ?? `{${paramName}}`);
        });
        return getStringExpressionLabel(builder.expression, {
          allowDynamic,
          allowBareIdentifierDynamic,
          paramValues: nextParamValues,
          depth: depth + 1,
        });
      }
    }

    return allowDynamic ? `{${compactExpressionText(current)}}` : null;
  }

  function normalizeBrowserStorageKey(label) {
    return label
      .replace(/draft_\{(?:activeId|id|scenarioIdToSave)\}/g, "draft_{scenarioId}")
      .replace(/rpg_scenario_v3_\{(?:id|scenarioId)\}/g, "rpg_scenario_v3_{scenarioId}")
      .replace(/\{scenarioIdToSave\}/g, "{scenarioId}")
      .replace(/\{activeId\}/g, "{scenarioId}");
  }

  function getReturnExpressionFromBlock(body) {
    const returnStatement = body.statements.find((statement) => ts.isReturnStatement(statement));
    return returnStatement?.expression ?? null;
  }

  function collectStringReturnFunctions(node) {
    if (ts.isFunctionDeclaration(node) && node.name && node.body) {
      const expression = getReturnExpressionFromBlock(node.body);
      if (expression) {
        stringReturnFunctions.set(node.name.text, {
          params: node.parameters
            .map((param) => ts.isIdentifier(param.name) ? param.name.text : null)
            .filter(Boolean),
          expression,
        });
      }
    }

    if (
      ts.isVariableDeclaration(node) &&
      ts.isIdentifier(node.name) &&
      node.initializer &&
      (ts.isArrowFunction(node.initializer) || ts.isFunctionExpression(node.initializer))
    ) {
      const body = node.initializer.body;
      const expression = ts.isBlock(body) ? getReturnExpressionFromBlock(body) : body;
      if (expression) {
        stringReturnFunctions.set(node.name.text, {
          params: node.initializer.parameters
            .map((param) => ts.isIdentifier(param.name) ? param.name.text : null)
            .filter(Boolean),
          expression,
        });
      }
    }

    ts.forEachChild(node, collectStringReturnFunctions);
  }

  function getStringLikeArg(node, options = {}) {
    if (node.arguments.length === 0) return null;
    const [firstArg] = node.arguments;
    return getStringExpressionLabel(firstArg, options);
  }

  function findSupabaseTarget(expr) {
    const current = unwrapExpression(expr);

    if (ts.isCallExpression(current) && ts.isPropertyAccessExpression(current.expression)) {
      const propertyName = current.expression.name.text;
      const stringArg = getStringLikeArg(current);

      if (stringArg) {
        if (
          propertyName === "from" &&
          ts.isPropertyAccessExpression(current.expression.expression) &&
          current.expression.expression.name.text === "storage"
        ) {
          return { kind: "bucket", name: stringArg };
        }

        if (propertyName === "from") {
          return { kind: "table", name: stringArg };
        }
      }

      return findSupabaseTarget(current.expression.expression);
    }

    if (ts.isPropertyAccessExpression(current)) {
      return findSupabaseTarget(current.expression);
    }

    return null;
  }

  function findBrowserStorageKind(expr) {
    const current = unwrapExpression(expr);

    if (ts.isPropertyAccessExpression(current)) {
      const name = current.name.text;
      if (name === "localStorage" || name === "sessionStorage") return name;
      return findBrowserStorageKind(current.expression);
    }

    if (ts.isIdentifier(current)) {
      if (current.text === "localStorage" || current.text === "sessionStorage") {
        return current.text;
      }
    }

    return null;
  }

  function pushBrowserStorageAccess(kind, accessType, key) {
    const normalizedKey = normalizeBrowserStorageKey(key);
    if (kind === "localStorage") {
      if (accessType === "read") {
        localStorageReads.push(normalizedKey);
      } else {
        localStorageWrites.push(normalizedKey);
      }
    }

    if (kind === "sessionStorage") {
      if (accessType === "read") {
        sessionStorageReads.push(normalizedKey);
      } else {
        sessionStorageWrites.push(normalizedKey);
      }
    }
  }

  function collectBrowserStorageProxyFunctions(node) {
    function registerProxyFunction(name, parameters, body) {
      const parameterIndexes = new Map(parameters.map((paramName, index) => [paramName, index]));
      const operations = [];

      function visitProxy(candidate) {
        if (
          ts.isCallExpression(candidate) &&
          ts.isPropertyAccessExpression(candidate.expression) &&
          candidate.arguments.length > 0
        ) {
          const propertyName = candidate.expression.name.text;
          const storageKind = findBrowserStorageKind(candidate.expression.expression);
          const firstArg = unwrapExpression(candidate.arguments[0]);
          if (storageKind && ts.isIdentifier(firstArg) && parameterIndexes.has(firstArg.text)) {
            if (BROWSER_STORAGE_READ_METHODS.has(propertyName)) {
              operations.push({ kind: storageKind, accessType: "read", paramIndex: parameterIndexes.get(firstArg.text) });
            }
            if (BROWSER_STORAGE_WRITE_METHODS.has(propertyName)) {
              operations.push({ kind: storageKind, accessType: "write", paramIndex: parameterIndexes.get(firstArg.text) });
            }
          }
        }

        ts.forEachChild(candidate, visitProxy);
      }

      visitProxy(body);

      if (operations.length > 0) {
        browserStorageProxyFunctions.set(name, operations);
      }
    }

    if (ts.isFunctionDeclaration(node) && node.name && node.body) {
      registerProxyFunction(
        node.name.text,
        node.parameters
          .map((param) => ts.isIdentifier(param.name) ? param.name.text : null)
          .filter(Boolean),
        node.body,
      );
    }

    if (
      ts.isVariableDeclaration(node) &&
      ts.isIdentifier(node.name) &&
      node.initializer &&
      (ts.isArrowFunction(node.initializer) || ts.isFunctionExpression(node.initializer))
    ) {
      registerProxyFunction(
        node.name.text,
        node.initializer.parameters
          .map((param) => ts.isIdentifier(param.name) ? param.name.text : null)
          .filter(Boolean),
        node.initializer.body,
      );
    }

    ts.forEachChild(node, collectBrowserStorageProxyFunctions);
  }

  collectStringConstants(sourceFile);
  collectStringReturnFunctions(sourceFile);
  collectBrowserStorageProxyFunctions(sourceFile);

  function visit(node) {
    if (ts.isImportDeclaration(node) || ts.isExportDeclaration(node)) {
      const moduleSpecifier = node.moduleSpecifier;
      if (moduleSpecifier && ts.isStringLiteralLike(moduleSpecifier)) {
        const resolved = resolveImportSpecifier(absPath, moduleSpecifier.text);
        if (resolved) imports.push(toWebPath(resolved));
      }
    }

    if (ts.isCallExpression(node)) {
      if (ts.isIdentifier(node.expression)) {
        const proxyOperations = browserStorageProxyFunctions.get(node.expression.text);
        if (proxyOperations) {
          proxyOperations.forEach((operation) => {
            const arg = node.arguments[operation.paramIndex];
            if (!arg) return;
            const keyLabel = getStringExpressionLabel(arg, {
              allowDynamic: true,
              allowBareIdentifierDynamic: true,
            });
            if (keyLabel) {
              pushBrowserStorageAccess(operation.kind, operation.accessType, keyLabel);
            }
          });
        }
      }

      if (node.expression.kind === ts.SyntaxKind.ImportKeyword) {
        const dynamicSpecifier = getStringArg(node);
        if (dynamicSpecifier) {
          const resolved = resolveImportSpecifier(absPath, dynamicSpecifier);
          if (resolved) imports.push(toWebPath(resolved));
        }
      } else if (ts.isPropertyAccessExpression(node.expression)) {
        const propertyName = node.expression.name.text;
        const stringArg = getStringLikeArg(node);

        if (stringArg) {
          if (
            propertyName === "from" &&
            ts.isPropertyAccessExpression(node.expression.expression) &&
            node.expression.expression.name.text === "storage"
          ) {
            storageBuckets.push(stringArg);
          } else if (propertyName === "from") {
            tables.push(stringArg);
          } else if (propertyName === "rpc") {
            rpcs.push(stringArg);
          } else if (propertyName === "invoke") {
            edgeFunctions.push(stringArg);
          }
        }

        const target = findSupabaseTarget(node.expression.expression);
        if (target?.kind === "table") {
          tables.push(target.name);
          if (TABLE_READ_METHODS.has(propertyName)) {
            tableReads.push(target.name);
          }
          if (TABLE_WRITE_METHODS.has(propertyName)) {
            tableWrites.push(target.name);
          }
        }

        if (target?.kind === "bucket") {
          storageBuckets.push(target.name);
          if (STORAGE_READ_METHODS.has(propertyName)) {
            storageReads.push(target.name);
          }
          if (STORAGE_WRITE_METHODS.has(propertyName)) {
            storageWrites.push(target.name);
          }
        }

        const browserStorageKind = findBrowserStorageKind(node.expression.expression);
        const browserStorageKey = getStringLikeArg(node, { allowDynamic: true });
        const prefixKeys = [];
        if (!browserStorageKey && propertyName === "removeItem" && node.arguments.length > 0) {
          const firstArg = unwrapExpression(node.arguments[0]);
          if (ts.isIdentifier(firstArg)) {
            for (const [name, values] of stringArrayConstants.entries()) {
              if (/prefix/i.test(name)) {
                values.forEach((value) => prefixKeys.push(`${value}*`));
              }
            }
          }
        }

        if (browserStorageKind && browserStorageKey) {
          if (browserStorageKind === "localStorage") {
            if (BROWSER_STORAGE_READ_METHODS.has(propertyName)) {
              pushBrowserStorageAccess(browserStorageKind, "read", browserStorageKey);
            }
            if (BROWSER_STORAGE_WRITE_METHODS.has(propertyName)) {
              pushBrowserStorageAccess(browserStorageKind, "write", browserStorageKey);
            }
          }

          if (browserStorageKind === "sessionStorage") {
            if (BROWSER_STORAGE_READ_METHODS.has(propertyName)) {
              pushBrowserStorageAccess(browserStorageKind, "read", browserStorageKey);
            }
            if (BROWSER_STORAGE_WRITE_METHODS.has(propertyName)) {
              pushBrowserStorageAccess(browserStorageKind, "write", browserStorageKey);
            }
          }
        }

        if (browserStorageKind && prefixKeys.length > 0 && BROWSER_STORAGE_WRITE_METHODS.has(propertyName)) {
          prefixKeys.forEach((key) => pushBrowserStorageAccess(browserStorageKind, "write", key));
        }
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(sourceFile);

  return {
    imports: uniqueSorted(imports),
    tables: uniqueSorted(tables),
    tableReads: uniqueSorted(tableReads),
    tableWrites: uniqueSorted(tableWrites),
    rpcs: uniqueSorted(rpcs),
    edgeFunctions: uniqueSorted(edgeFunctions),
    storageBuckets: uniqueSorted(storageBuckets),
    storageReads: uniqueSorted(storageReads),
    storageWrites: uniqueSorted(storageWrites),
    localStorageReads: uniqueSorted(localStorageReads),
    localStorageWrites: uniqueSorted(localStorageWrites),
    sessionStorageReads: uniqueSorted(sessionStorageReads),
    sessionStorageWrites: uniqueSorted(sessionStorageWrites),
  };
}

function resolveImportSpecifier(fromAbsPath, specifier) {
  if (!specifier) return null;
  if (specifier.startsWith("@/")) {
    const target = path.join(srcRoot, specifier.slice(2));
    return resolveCandidate(target);
  }
  if (specifier.startsWith("./") || specifier.startsWith("../")) {
    const target = path.resolve(path.dirname(fromAbsPath), specifier);
    return resolveCandidate(target);
  }
  return null;
}

function resolveCandidate(basePath) {
  const candidates = [
    basePath,
    `${basePath}.ts`,
    `${basePath}.tsx`,
    `${basePath}.js`,
    `${basePath}.jsx`,
    `${basePath}.json`,
    path.join(basePath, "index.ts"),
    path.join(basePath, "index.tsx"),
    path.join(basePath, "index.js"),
    path.join(basePath, "index.jsx"),
  ];

  for (const candidate of candidates) {
    if (fs.existsSync(candidate) && fs.statSync(candidate).isFile()) {
      return candidate;
    }
  }
  return null;
}

function extractMatches(text, pattern, groupIndex = 1) {
  const matches = [];
  for (const match of text.matchAll(pattern)) {
    const value = match[groupIndex];
    if (value) matches.push(value.trim());
  }
  return matches;
}

