#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import ts from "typescript";

const projectRoot = process.cwd();
const srcRoot = path.join(projectRoot, "src");
const docsRoot = path.join(projectRoot, "docs");
const supabaseRoot = path.join(projectRoot, "supabase");
const publicRoot = path.join(projectRoot, "public");

const sourceExtensions = new Set([
  ".ts",
  ".tsx",
  ".css",
  ".scss",
  ".json",
  ".md",
  ".sql",
  ".html",
]);

const extraExtensions = new Set([
  ".ts",
  ".tsx",
  ".sql",
  ".md",
  ".json",
  ".html",
  ".toml",
  ".svg",
  ".ico",
  ".txt",
]);

function walk(dir, options = {}) {
  const { maxDepth = Number.POSITIVE_INFINITY, depth = 0 } = options;
  if (!fs.existsSync(dir)) return [];
  if (depth > maxDepth) return [];

  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(abs, { maxDepth, depth: depth + 1 }));
      continue;
    }
    files.push(abs);
  }

  return files;
}

function toWebPath(absPath) {
  const relative = path.relative(projectRoot, absPath).split(path.sep).join("/");
  return `/${relative}`;
}

function toTsArrayExport(variableName, values, sourceLabel) {
  const lines = [];
  lines.push(`// Auto-generated by scripts/refresh-architecture-paths.mjs from ${sourceLabel}.`);
  lines.push("// Do not hand-edit this file; regenerate it instead.");
  lines.push(`export const ${variableName}: string[] = [`);
  values.forEach((value) => lines.push(`  '${value}',`));
  lines.push("];");
  lines.push("");
  return lines.join("\n");
}

function countLines(absPath) {
  const text = fs.readFileSync(absPath, "utf8");
  if (!text) return 0;
  return text.split(/\r?\n/).length;
}

function toMetricsExport(variableName, values, sourceLabel) {
  const lines = [];
  lines.push(`// Auto-generated by scripts/refresh-architecture-paths.mjs from ${sourceLabel}.`);
  lines.push("// Do not hand-edit this file; regenerate it instead.");
  lines.push(
    `export const ${variableName}: Record<string, { lineCount: number }> = {`,
  );
  values.forEach(({ webPath, lineCount }) =>
    lines.push(`  '${webPath}': { lineCount: ${lineCount} },`),
  );
  lines.push("};");
  lines.push("");
  return lines.join("\n");
}

function toAnalysisExport(variableName, values, sourceLabel) {
  const lines = [];
  lines.push(`// Auto-generated by scripts/refresh-architecture-paths.mjs from ${sourceLabel}.`);
  lines.push("// Do not hand-edit this file; regenerate it instead.");
  lines.push(
    `export const ${variableName}: Record<string, { imports: string[]; importedBy: string[]; tables: string[]; rpcs: string[]; edgeFunctions: string[]; storageBuckets: string[] }> = {`,
  );
  values.forEach(({ webPath, imports, importedBy, tables, rpcs, edgeFunctions, storageBuckets }) => {
    lines.push(
      `  '${webPath}': { imports: ${JSON.stringify(imports)}, importedBy: ${JSON.stringify(importedBy)}, tables: ${JSON.stringify(tables)}, rpcs: ${JSON.stringify(rpcs)}, edgeFunctions: ${JSON.stringify(edgeFunctions)}, storageBuckets: ${JSON.stringify(storageBuckets)} },`,
    );
  });
  lines.push("};");
  lines.push("");
  return lines.join("\n");
}

function isCodeLikeFile(absPath) {
  const ext = path.extname(absPath).toLowerCase();
  return [
    ".ts",
    ".tsx",
    ".js",
    ".jsx",
    ".mjs",
    ".sql",
    ".md",
    ".json",
    ".html",
    ".css",
    ".scss",
    ".toml",
    ".txt",
    ".svg",
  ].includes(ext);
}

function readTextIfPossible(absPath) {
  if (!isCodeLikeFile(absPath)) return "";
  try {
    return fs.readFileSync(absPath, "utf8");
  } catch {
    return "";
  }
}

function uniqueSorted(values) {
  return Array.from(new Set(values.filter(Boolean))).sort((a, b) => a.localeCompare(b));
}

function isAstCodeFile(absPath) {
  return [".ts", ".tsx", ".js", ".jsx", ".mjs"].includes(path.extname(absPath).toLowerCase());
}

function getScriptKind(absPath) {
  const ext = path.extname(absPath).toLowerCase();
  if (ext === ".tsx") return ts.ScriptKind.TSX;
  if (ext === ".jsx") return ts.ScriptKind.JSX;
  if (ext === ".js" || ext === ".mjs") return ts.ScriptKind.JS;
  return ts.ScriptKind.TS;
}

function getStringArg(node) {
  if (node.arguments.length === 0) return null;
  const [firstArg] = node.arguments;
  return ts.isStringLiteralLike(firstArg) ? firstArg.text.trim() : null;
}

function extractAstRelationships(absPath, text) {
  if (!isAstCodeFile(absPath) || !text.trim()) {
    return {
      imports: [],
      tables: [],
      rpcs: [],
      edgeFunctions: [],
      storageBuckets: [],
    };
  }

  const sourceFile = ts.createSourceFile(
    absPath,
    text,
    ts.ScriptTarget.Latest,
    true,
    getScriptKind(absPath),
  );

  const imports = [];
  const tables = [];
  const rpcs = [];
  const edgeFunctions = [];
  const storageBuckets = [];

  function visit(node) {
    if (ts.isImportDeclaration(node) || ts.isExportDeclaration(node)) {
      const moduleSpecifier = node.moduleSpecifier;
      if (moduleSpecifier && ts.isStringLiteralLike(moduleSpecifier)) {
        const resolved = resolveImportSpecifier(absPath, moduleSpecifier.text);
        if (resolved) imports.push(toWebPath(resolved));
      }
    }

    if (ts.isCallExpression(node)) {
      if (node.expression.kind === ts.SyntaxKind.ImportKeyword) {
        const dynamicSpecifier = getStringArg(node);
        if (dynamicSpecifier) {
          const resolved = resolveImportSpecifier(absPath, dynamicSpecifier);
          if (resolved) imports.push(toWebPath(resolved));
        }
      } else if (ts.isPropertyAccessExpression(node.expression)) {
        const propertyName = node.expression.name.text;
        const stringArg = getStringArg(node);

        if (stringArg) {
          if (
            propertyName === "from" &&
            ts.isPropertyAccessExpression(node.expression.expression) &&
            node.expression.expression.name.text === "storage"
          ) {
            storageBuckets.push(stringArg);
          } else if (propertyName === "from") {
            tables.push(stringArg);
          } else if (propertyName === "rpc") {
            rpcs.push(stringArg);
          } else if (propertyName === "invoke") {
            edgeFunctions.push(stringArg);
          }
        }
      }
    }

    ts.forEachChild(node, visit);
  }

  visit(sourceFile);

  return {
    imports: uniqueSorted(imports),
    tables: uniqueSorted(tables),
    rpcs: uniqueSorted(rpcs),
    edgeFunctions: uniqueSorted(edgeFunctions),
    storageBuckets: uniqueSorted(storageBuckets),
  };
}

function resolveImportSpecifier(fromAbsPath, specifier) {
  if (!specifier) return null;
  if (specifier.startsWith("@/")) {
    const target = path.join(srcRoot, specifier.slice(2));
    return resolveCandidate(target);
  }
  if (specifier.startsWith("./") || specifier.startsWith("../")) {
    const target = path.resolve(path.dirname(fromAbsPath), specifier);
    return resolveCandidate(target);
  }
  return null;
}

function resolveCandidate(basePath) {
  const candidates = [
    basePath,
    `${basePath}.ts`,
    `${basePath}.tsx`,
    `${basePath}.js`,
    `${basePath}.jsx`,
    `${basePath}.json`,
    path.join(basePath, "index.ts"),
    path.join(basePath, "index.tsx"),
    path.join(basePath, "index.js"),
    path.join(basePath, "index.jsx"),
  ];

  for (const candidate of candidates) {
    if (fs.existsSync(candidate) && fs.statSync(candidate).isFile()) {
      return candidate;
    }
  }
  return null;
}

function extractMatches(text, pattern, groupIndex = 1) {
  const matches = [];
  for (const match of text.matchAll(pattern)) {
    const value = match[groupIndex];
    if (value) matches.push(value.trim());
  }
  return matches;
}

const sourceAbsPaths = walk(srcRoot)
  .filter((abs) => sourceExtensions.has(path.extname(abs).toLowerCase()))
  .sort((a, b) => a.localeCompare(b));

const sourcePaths = sourceAbsPaths
  .map(toWebPath)
  .sort((a, b) => a.localeCompare(b));

const extraAbsPaths = [
  ...walk(docsRoot).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
  ...walk(supabaseRoot).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
  ...walk(publicRoot, { maxDepth: 1 }).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
].sort((a, b) => a.localeCompare(b));

const extraPaths = extraAbsPaths
  .map(toWebPath)
  .sort((a, b) => a.localeCompare(b));

const sourceOutput = toTsArrayExport("architectureSourcePaths", sourcePaths, "src");
const extraOutput = toTsArrayExport("architectureExtraPaths", extraPaths, "docs/supabase/public");
const metricsEntries = [...sourceAbsPaths, ...extraAbsPaths]
  .map((absPath) => ({
    webPath: toWebPath(absPath),
    lineCount: countLines(absPath),
  }))
  .sort((a, b) => a.webPath.localeCompare(b.webPath));
const metricsOutput = toMetricsExport(
  "architectureFileMetrics",
  metricsEntries,
  "src/docs/supabase/public",
);

const analysisByPath = new Map();

[...sourceAbsPaths, ...extraAbsPaths].forEach((absPath) => {
  const text = readTextIfPossible(absPath);
  const {
    imports,
    tables,
    rpcs,
    edgeFunctions,
    storageBuckets,
  } = extractAstRelationships(absPath, text);

  analysisByPath.set(toWebPath(absPath), {
    imports,
    importedBy: [],
    tables,
    rpcs,
    edgeFunctions,
    storageBuckets,
  });
});

for (const [webPath, analysis] of analysisByPath.entries()) {
  analysis.imports.forEach((importedPath) => {
    const target = analysisByPath.get(importedPath);
    if (!target) return;
    target.importedBy.push(webPath);
  });
}

const analysisEntries = Array.from(analysisByPath.entries())
  .map(([webPath, analysis]) => ({
    webPath,
    imports: uniqueSorted(analysis.imports),
    importedBy: uniqueSorted(analysis.importedBy),
    tables: analysis.tables,
    rpcs: analysis.rpcs,
    edgeFunctions: analysis.edgeFunctions,
    storageBuckets: analysis.storageBuckets,
  }))
  .sort((a, b) => a.webPath.localeCompare(b.webPath));

const analysisOutput = toAnalysisExport(
  "architectureFileAnalysis",
  analysisEntries,
  "src/docs/supabase/public",
);

fs.writeFileSync(path.join(srcRoot, "data", "architecture-source-paths.ts"), sourceOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-extra-paths.ts"), extraOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-file-metrics.ts"), metricsOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-file-analysis.ts"), analysisOutput, "utf8");

console.log(
  `Generated ${sourcePaths.length} source paths, ${extraPaths.length} extra paths, ${metricsEntries.length} line-count metrics, and ${analysisEntries.length} relationship analyses.`,
);
