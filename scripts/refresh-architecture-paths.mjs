#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const projectRoot = process.cwd();
const srcRoot = path.join(projectRoot, "src");
const docsRoot = path.join(projectRoot, "docs");
const supabaseRoot = path.join(projectRoot, "supabase");
const publicRoot = path.join(projectRoot, "public");

const sourceExtensions = new Set([
  ".ts",
  ".tsx",
  ".css",
  ".scss",
  ".json",
  ".md",
  ".sql",
  ".html",
]);

const extraExtensions = new Set([
  ".ts",
  ".tsx",
  ".sql",
  ".md",
  ".json",
  ".html",
  ".toml",
  ".svg",
  ".ico",
  ".txt",
]);

function walk(dir, options = {}) {
  const { maxDepth = Number.POSITIVE_INFINITY, depth = 0 } = options;
  if (!fs.existsSync(dir)) return [];
  if (depth > maxDepth) return [];

  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(abs, { maxDepth, depth: depth + 1 }));
      continue;
    }
    files.push(abs);
  }

  return files;
}

function toWebPath(absPath) {
  const relative = path.relative(projectRoot, absPath).split(path.sep).join("/");
  return `/${relative}`;
}

function toTsArrayExport(variableName, values, sourceLabel) {
  const lines = [];
  lines.push(`// Auto-generated by scripts/refresh-architecture-paths.mjs from ${sourceLabel}.`);
  lines.push("// Do not hand-edit this file; regenerate it instead.");
  lines.push(`export const ${variableName}: string[] = [`);
  values.forEach((value) => lines.push(`  '${value}',`));
  lines.push("];");
  lines.push("");
  return lines.join("\n");
}

const sourcePaths = walk(srcRoot)
  .filter((abs) => sourceExtensions.has(path.extname(abs).toLowerCase()))
  .map(toWebPath)
  .sort((a, b) => a.localeCompare(b));

const extraPaths = [
  ...walk(docsRoot).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
  ...walk(supabaseRoot).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
  ...walk(publicRoot, { maxDepth: 1 }).filter((abs) => extraExtensions.has(path.extname(abs).toLowerCase())),
]
  .map(toWebPath)
  .sort((a, b) => a.localeCompare(b));

const sourceOutput = toTsArrayExport("architectureSourcePaths", sourcePaths, "src");
const extraOutput = toTsArrayExport("architectureExtraPaths", extraPaths, "docs/supabase/public");

fs.writeFileSync(path.join(srcRoot, "data", "architecture-source-paths.ts"), sourceOutput, "utf8");
fs.writeFileSync(path.join(srcRoot, "data", "architecture-extra-paths.ts"), extraOutput, "utf8");

console.log(`Generated ${sourcePaths.length} source paths and ${extraPaths.length} extra paths.`);
