function finding(
  id: string,
  title: string,
  severity: QualityFinding["severity"],
  confidence: QualityFinding["confidence"],
  domain: QualityFinding["domain"],
  category: string,
  page: string,
  files: string[],
  problem: string,
  whyItMatters: string,
  userImpact: string,
  recommendation: string,
  fixLevel: QualityFinding["fixLevel"],
  difficulty: QualityFinding["implementationDifficulty"],
  runId: string,
  extra?: Partial<QualityFinding>,
): QualityFinding {
  return {
    id,
    title,
    severity,
    confidence,
    domain,
    category,
    status: "open",
    verificationStatus: "unverified",
    page,
    files,
    tags: [],
    evidence: [],
    currentState: problem,
    problem,
    whyItMatters,
    userImpact,
    recommendation,
    reproSteps: [],
    expectedBehavior: "",
    actualBehavior: "",
    sourceKind: "agent-scan",
    fixLevel,
    implementationDifficulty: difficulty,
    batchable: false,
    designSystemLevel: false,
    foundBy: stamp(runId),
    contributors: [codexAgent],
    relatedFindingIds: [],
    comments: [],
    createdAt: scanTimestamp,
    updatedAt: scanTimestamp,
    ...extra,
  };
}

