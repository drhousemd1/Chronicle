function summaryFor(runFindings: QualityFinding[]) {
  return {
    findingsTotal: runFindings.length,
    newFindings: runFindings.length,
    critical: runFindings.filter((f) => f.severity === "critical").length,
    high: runFindings.filter((f) => f.severity === "high").length,
    open: runFindings.filter((f) => f.status === "open" || f.status === "in-progress").length,
    verified: runFindings.filter((f) => f.status === "verified").length,
  };
}

